public class explicitCast{
    public static void main(String[] args){
        //Explicit casting
        double x = 1.1;
        int y = (int)x+2;
        System.out.println(y);

        int z = 2;
        double zz =z + 1;
        System.out.println(zz);

        
        short r = 23;
        short rr = 6;
        
        int rrr = (int)r + (int)rr;
        System.out.println(rrr);
    }
}