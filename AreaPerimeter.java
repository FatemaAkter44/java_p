public class AreaPerimeter{
    public static void main(String[] args){
        double length = 200;
        double width = 100;
        double area = length * width;
        double perimeter = 2 * (length * width);
    
        System.out.println("the area is : " + area);
        System.out.println("the perimeter : " + perimeter);  
    
    
    }
}