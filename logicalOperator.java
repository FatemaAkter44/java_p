public class logicalOperator{
    public static void main(String[] args){
        int x = 100;
        int y =1000;
        System.out.println(x=y);
        System.out.println(x!=y);
        System.out.println(x<=y);
        System.out.println(x>=y);
        System.out.println(x>y);
        System.out.println(x<y);
    }
}
