public class castingExe{
    public static void main(String[] args){
        //Explicit casting
        float x = 100.05f;
        int y = (int)x+2;
        System.out.println(y);

        int z = 2;
        double zz =z + 1;
        System.out.println(zz);

        
        long r = 22966778;
        float rr = 6.6f;
        
        int rrr = (int)r + (int)rr;
        System.out.println(rrr);
    }
}